class Shopping {
  int id;
  String name;
  String address;
  double lat;
  double lng;
  String image;

  Shopping(int id, String name, String address, double lat, double lng,
      String image) {
    this.id = id;
    this.name = name;
    this.address = address;
    this.lat = lat;
    this.lng = lng;
    this.image = image;
  }
}
